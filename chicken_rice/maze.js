function selectRandomMaze() {
    const randomIndex = Math.floor(Math.random() * maze.length);
    return maze[randomIndex];
    
}

let currentMaze = selectRandomMaze();

const player = { x: 1, y: 1 };
const exit = { x: currentMaze[0].length - 2, y: currentMaze.length - 2 };

const playerImg = document.getElementById("playerImg");
const exitImg = document.getElementById("exitImg");

function drawMaze() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    ctx.fillStyle = "white"; // Set the background color to white
    ctx.fillRect(0, 0, canvas.width, canvas.height); // Fill the entire canvas with white

    for (let y = 0; y < currentMaze.length; y++) {
        for (let x = 0; x < currentMaze[y].length; x++) {
            if (currentMaze[y][x] === 1) {
                ctx.fillStyle = "black";
                ctx.fillRect(x * cellSize, y * cellSize, cellSize, cellSize);
            }
        }
    }
    ctx.drawImage(exitImg, exit.x * cellSize, exit.y * cellSize, cellSize, cellSize);
}

function drawPlayer() {
    ctx.drawImage(playerImg, player.x * cellSize, player.y * cellSize, cellSize, cellSize);
}

function movePlayer(event) {
    const keyCode = event.keyCode;
    const newX = player.x + (keyCode === 39 ? 1 : keyCode === 37 ? -1 : 0);
    const newY = player.y + (keyCode === 40 ? 1 : keyCode === 38 ? -1 : 0);

    if (currentMaze[newY][newX] === 0) {
        player.x = newX;
        player.y = newY;
        drawMaze();
        drawPlayer();

        if (newX === exit.x && newY === exit.y) {
            redirectToResultPage()                                             //send to next state 
        }
    }
}

function redirectToResultPage() {
    document.getElementById('transition-container').style.animation = 'sliding-left-in 1s forwards'
    setTimeout(function(){
        window.location.href = '../meat/index.html';
    }, 1000)
}

function startGame() {
    document.addEventListener("keydown", movePlayer);
    drawMaze();
    drawPlayer();
}

startGame();